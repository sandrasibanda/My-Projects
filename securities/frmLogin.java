    private void mFetchUserDetails() {
        String strDBConnectionString = "jdbc:mysql://localhost:3306/gatecontrol";
        String strDBUser = "root";
        String strDBPassword = "Password";
        java.sql.Connection conMySQLConnectionString;
        Statement stStatement;
        ResultSet rs;
        try {
            conMySQLConnectionString = DriverManager.getConnection(strDBConnectionString, strDBUser, strDBPassword);
            stStatement = conMySQLConnectionString.createStatement();
            String strQuery="SELECT ID,Username,Password,Role from users_login where Username='"+txtUsername.getText().toUpperCase()+"'";
            stStatement.execute(strQuery);

            rs = stStatement.getResultSet();
            while (rs.next()) {
                strName = rs.getString(2);
                strPassword = rs.getString(3);
                strRole = rs.getString(4);

            }
            JOptionPane.showMessageDialog(null, "The database has been connected and you are logged in");
            //JOptionPane.showMessageDialog(null, "You are logged in");
            stStatement.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

