    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        frmMain frmM = new frmMain();
        if (txtUsername.getText().toLowerCase().equals("admin") && psfPassword.getText().toLowerCase().equals("admin")
                && cboRole.getSelectedItem().toString().toLowerCase().equals("standard")) {
            frmM.strRole = "STANDARD";
            frmM.mSetRoleRights();
            frmM.setVisible(true);
            this.setVisible(false);
            frmM.setTitle("You are logged in as:" + " " + txtUsername.getText().toUpperCase());
            frmM.setExtendedState(MAXIMIZED_BOTH);
        } else {
            try {
                mEncryptPassword();
                mFetchUserDetails();
                mDecryptPassword();
                if (txtUsername.getText().toUpperCase().equals(strName.toUpperCase()) && psfPassword.getText().toUpperCase().equals(strPassword.toUpperCase())
                        && strRole.toUpperCase().equals(cboRole.getSelectedItem().toString().toUpperCase())) {
                    frmM.strRole = cboRole.getSelectedItem().toString().toUpperCase();
                    frmM.mSetRoleRights();
                    frmM.setVisible(true);
                    this.setVisible(false);
                    frmM.setTitle("You are logged in as:" + " " + txtUsername.getText().toUpperCase());
                    frmM.setExtendedState(MAXIMIZED_BOTH);
                } else {
                    JOptionPane.showMessageDialog(null, "");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "ACCESS DENIED!! Check Username, Password or Role");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

